/**
 * JSON Reporter - Formats the bug report as structured JSON
 */

function generateJSONReport(report) {
  return JSON.stringify(report, null, 2);
}

function generateMarkdownReport(report) {
  const { url, timestamp, duration_ms, testsRun, summary, vitals, sourceAudit, bugs } = report;

  let md = `# Playwright QA Suite — Bug Report\n\n`;
  md += `**URL:** ${url}\n`;
  md += `**Date:** ${new Date(timestamp).toLocaleString()}\n`;
  md += `**Duration:** ${(duration_ms / 1000).toFixed(1)}s\n`;
  md += `**Tests Run:** ${testsRun}\n\n`;

  md += `## Executive Summary\n\n`;
  md += `| Severity | Count |\n`;
  md += `|----------|-------|\n`;
  Object.entries(summary).forEach(([sev, count]) => {
    const emoji = { critical: '🔴', high: '🟠', medium: '🟡', low: '🟢', info: '⚪' }[sev] || '';
    md += `| ${emoji} ${sev.charAt(0).toUpperCase() + sev.slice(1)} | ${count} |\n`;
  });
  md += `\n`;

  md += `## Core Web Vitals\n\n`;
  md += `| Metric | Value | Rating |\n`;
  md += `|--------|-------|--------|\n`;
  Object.entries(vitals).forEach(([name, data]) => {
    const rating = { good: '✅', 'needs-improvement': '⚠️', poor: '❌' }[data.rating] || '❓';
    md += `| ${name.toUpperCase()} | ${data.value}${data.unit} | ${rating} ${data.rating} |\n`;
  });
  md += `\n`;

  md += `## Source Audit\n\n`;
  md += `- **Third-party Scripts:** ${sourceAudit.thirdPartyScripts}\n`;
  md += `- **Analytics Providers:** ${sourceAudit.analyticsProviders?.join(', ') || 'None'}\n`;
  md += `- **Inline Event Handlers:** ${sourceAudit.inlineEventHandlers}\n`;
  md += `- **Console Errors:** ${sourceAudit.consoleErrors}\n`;
  md += `- **Missing Meta Tags:** ${sourceAudit.missingMetaTags?.join(', ') || 'None'}\n`;
  md += `- **Total DOM Nodes:** ${sourceAudit.totalDomNodes}\n`;
  md += `\n`;

  md += `## Issues\n\n`;

  const sortedBugs = [...bugs].sort((a, b) => {
    const order = { critical: 0, high: 1, medium: 2, low: 3, info: 4 };
    return order[a.severity] - order[b.severity];
  });

  sortedBugs.forEach((bug, index) => {
    const sevEmoji = { critical: '🔴', high: '🟠', medium: '🟡', low: '🟢', info: '⚪' }[bug.severity] || '';

    md += `### ${sevEmoji} [${bug.severity.toUpperCase()}] ${bug.title}\n\n`;
    md += `**Category:** ${bug.category}\n\n`;
    md += `${bug.description}\n\n`;

    if (bug.stepsToReproduce?.length > 0) {
      md += `**Steps to Reproduce:**\n`;
      bug.stepsToReproduce.forEach((step, i) => {
        md += `${i + 1}. ${step}\n`;
      });
      md += `\n`;
    }

    md += `**Expected:** ${bug.expected}\n\n`;
    md += `**Actual:** ${bug.actual}\n\n`;

    if (bug.url) {
      md += `**URL:** ${bug.url}\n\n`;
    }

    md += `---\n\n`;
  });

  md += `\n---\n`;
  md += `*Report generated by Playwright QA Suite*\n`;

  return md;
}

module.exports = { generateJSONReport, generateMarkdownReport };
