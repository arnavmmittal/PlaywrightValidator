const PDFDocument = require('pdfkit');

const COLORS = {
  critical: '#FF2D2D',
  high: '#FF6B35',
  medium: '#F5A623',
  low: '#4ECDC4',
  info: '#7B8794',
  good: '#4ECDC4',
  'needs-improvement': '#F5A623',
  poor: '#FF2D2D',
};

async function generatePDF(report) {
  return new Promise((resolve, reject) => {
    try {
      const doc = new PDFDocument({ margin: 50 });
      const chunks = [];

      doc.on('data', chunk => chunks.push(chunk));
      doc.on('end', () => resolve(Buffer.concat(chunks)));
      doc.on('error', reject);

      const { url, timestamp, duration_ms, testsRun, summary, vitals, sourceAudit, bugs } = report;

      // Header
      doc.fontSize(24).text('Playwright QA Suite', { align: 'center' });
      doc.fontSize(16).text('Bug Report', { align: 'center' });
      doc.moveDown();

      // Meta info
      doc.fontSize(10).fillColor('#666');
      doc.text(`URL: ${url}`);
      doc.text(`Date: ${new Date(timestamp).toLocaleString()}`);
      doc.text(`Duration: ${(duration_ms / 1000).toFixed(1)} seconds`);
      doc.text(`Tests Run: ${testsRun}`);
      doc.moveDown();

      // Severity Summary
      doc.fontSize(14).fillColor('#000').text('Summary');
      doc.moveDown(0.5);

      const severities = ['critical', 'high', 'medium', 'low', 'info'];
      const summaryText = severities
        .map(sev => `${sev.charAt(0).toUpperCase() + sev.slice(1)}: ${summary[sev]}`)
        .join('  |  ');
      doc.fontSize(10).text(summaryText);
      doc.moveDown();

      // Core Web Vitals
      doc.fontSize(14).fillColor('#000').text('Core Web Vitals');
      doc.moveDown(0.5);

      Object.entries(vitals).forEach(([name, data]) => {
        const color = COLORS[data.rating] || '#000';
        doc.fontSize(10).fillColor(color);
        doc.text(`${name.toUpperCase()}: ${data.value}${data.unit} (${data.rating})`);
      });
      doc.moveDown();

      // Source Audit
      doc.fontSize(14).fillColor('#000').text('Source Audit');
      doc.moveDown(0.5);
      doc.fontSize(10).fillColor('#666');
      doc.text(`Third-party Scripts: ${sourceAudit.thirdPartyScripts}`);
      doc.text(`Console Errors: ${sourceAudit.consoleErrors}`);
      doc.text(`Inline Event Handlers: ${sourceAudit.inlineEventHandlers}`);
      doc.text(`Total DOM Nodes: ${sourceAudit.totalDomNodes}`);
      if (sourceAudit.missingMetaTags?.length > 0) {
        doc.text(`Missing Meta Tags: ${sourceAudit.missingMetaTags.join(', ')}`);
      }
      doc.moveDown();

      // Issues
      doc.fontSize(14).fillColor('#000').text('Issues');
      doc.moveDown(0.5);

      const sortedBugs = [...bugs].sort((a, b) => {
        const order = { critical: 0, high: 1, medium: 2, low: 3, info: 4 };
        return order[a.severity] - order[b.severity];
      });

      sortedBugs.forEach((bug, index) => {
        // Check if we need a new page
        if (doc.y > 700) {
          doc.addPage();
        }

        const sevColor = COLORS[bug.severity] || '#000';

        doc.fontSize(11).fillColor(sevColor);
        doc.text(`[${bug.severity.toUpperCase()}] ${bug.title}`, { continued: false });

        doc.fontSize(9).fillColor('#666');
        doc.text(bug.description);
        doc.moveDown(0.3);

        if (bug.stepsToReproduce?.length > 0) {
          doc.text('Steps to Reproduce:');
          bug.stepsToReproduce.forEach((step, i) => {
            doc.text(`  ${i + 1}. ${step}`);
          });
        }

        doc.moveDown(0.3);
        doc.fillColor('#4ECDC4').text(`Expected: ${bug.expected}`);
        doc.fillColor('#FF6B35').text(`Actual: ${bug.actual}`);

        doc.moveDown();
      });

      // Footer
      doc.fontSize(8).fillColor('#999');
      doc.text(
        `Generated by Playwright QA Suite on ${new Date().toISOString()}`,
        50,
        doc.page.height - 50,
        { align: 'center' }
      );

      doc.end();
    } catch (error) {
      reject(error);
    }
  });
}

module.exports = { generatePDF };
